"use strict";
function getFirstElement(arr) {
    return arr[0];
}
let ans1 = getFirstElement([1, 2, 3]);
console.log(ans1); // Output: 1
let ans2 = getFirstElement(["one", "two", "three"]);
console.log(ans2); // Output: "one"
const str = "ABC";
const lowercaseStr = str.toLocaleLowerCase();
console.log(lowercaseStr); // Output: "abc"
